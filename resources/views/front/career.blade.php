<script>
$(document).ready(function () {
    const submitButton = document.getElementById('careerAppSubmit');
    // Captcha controls
    const captchaError = document.getElementById('custom_captcha_error_comman_form');
    const captchaImage = document.getElementById('captcha-image-comman-form');
    const reloadButton = document.getElementById('reload-button-comman-form');

    reloadButton.addEventListener('click', function () {
        captchaImage.src = '{{ route("captcha.image") }}?' + Date.now();
    });
    
    // Custom method: letters only
    $.validator.addMethod("lettersonly", function (value, element) {
        return this.optional(element) || /^[a-zA-Z\s]+$/.test(value);
    }, "Letters only please");
    
    $.validator.addMethod("maxfilesize", function (value, element, size) {
        if (this.optional(element)) {
            return true;
        }
    
        if (element.files && element.files.length) {
            return element.files[0].size <= size;
        }
    
        return false;
    }, "File size must be less than 2 MB");
    
    $.validator.addMethod("noSpamEmail", function (value, element) {
        const spamPatterns = [
            /^[a-zA-Z]{8,}[0-9]{6,}@/,
            /^[0-9]+@/,
            /(temp-mail|10minutemail|mailinator|guerrillamail|yopmail|throwawaymail|form-check.online|seismologiomail|ru|mailport.lat)/i,
            /^(test|demo|example|noreply|fake|admin|info|random|dummy)/i,
            /^(.)(\1){5,}@/
        ];
    
        for (let pattern of spamPatterns) {
            if (pattern.test(value)) {
                return false;
            }
        }
        return true;
    }, "This email is not allowed");
    
    $.validator.addMethod("captchaRequired", function (value) {
        return value && value.trim().length === 4;
    }, "Captcha must be 4 digits");
    
    $.validator.addMethod("uniqueEmail", function(value, element) {
        var isSuccess = false;
        $.ajax({
            url: '{{ route("check.application.email") }}',
            type: 'POST',
            data: {
                _token: '{{ csrf_token() }}',
                email: value
            },
            dataType: 'json',
            async: false, // Make synchronous to block form until check finishes
            success: function(response) {
                isSuccess = response.unique; // true if email is unique
            }
        });
        return isSuccess;
    }, "This email is already taken.");

    
    $("#applyForm").validate({
        ignore: [],
        rules: {
            full_name: {
                required: true,
                minlength: 3,
                lettersonly: true
            },
            email: {
                required: true,
                email: true,
                noSpamEmail: true,
                uniqueEmail: true
            },
            mobile_number: {
                required: true,
                digits: true,
                
                maxlength: 15
            },
            message:{
                'maxlength': 500,
            },
            resume: {
                required: true,
                extension: "pdf|doc|docx",
                maxfilesize: 2097152
            },
            custom_captcha_comman_form: {
                required: true,
                captchaRequired: true
            }
        },
    
        messages: {
            full_name: {
                required: "Please enter your full name",
                minlength: "Name must be at least 3 characters",
                lettersonly: "Only letters and spaces are allowed"
            },
            email: {
                required: "Please enter your email address",
                email: "Please enter a valid email address",
                noSpamEmail: "This email address is not allowed"
            },
            mobile_number: {
                required: "Please enter your mobile number",
                digits: "Only numbers are allowed",
                
                maxlength: "Mobile number must not exceed 15 digits"
            },
            resume: {
                required: "Please upload your resume",
                extension: "Only PDF, DOC or DOCX files are allowed",
                maxfilesize: "Resume must be less than 2 MB"
            },
            custom_captcha_comman_form: {
                required: "Please enter the captcha",
                captchaRequired: "Captcha must be exactly 4 characters"
            }
        },
    
        errorElement: "small",
        errorClass: "text-danger",
        errorPlacement: function (error, element) {
            if (element.attr("name") === "custom_captcha_comman_form") {
                $("#custom_captcha_error_comman_form")
                    .html(error)
                    .show();
            } else {
                error.insertAfter(element);
            }
        },
        highlight: function (element) {
            $(element).addClass("is-invalid");
        },
        unhighlight: function (element) {
            $(element).removeClass("is-invalid");
        },
     
        submitHandler: function (form) {
            handleCareerCaptchaSubmit(form);
        }
    });

});

function handleCareerCaptchaSubmit(form) {

    const submitButton = $('#careerAppSubmit');
    const captchaInput = $('#custom_captcha_comman_form');
    const captchaError = $('#custom_captcha_error_comman_form');
    const captchaImage = $('#captcha-image-comman-form');
    $("#custom_captcha_error_comman_form").empty().hide();
    $("#custom_captcha_comman_form").removeClass("is-invalid");
    
    submitButton.prop('disabled', true).text('Verifying captcha...');

    $.ajax({
        url: '{{ route("captcha.verify") }}',
        type: 'POST',
        data: {
            _token: '{{ csrf_token() }}',
            custom_captcha: captchaInput.val().trim()
        },
        success: function (response) {

            if (response.success) {
                submitButton.text('Submitting...');
                form.submit();
            } else {
                captchaImage.attr('src', '{{ route("captcha.image") }}?' + Date.now());
                $("#custom_captcha_error_comman_form")
                    .html('<small class="text-danger">' + response.message + '</small>')
                    .show();
                $("#custom_captcha_comman_form").addClass("is-invalid");
                submitButton.prop('disabled', false).text('Submit');
            }
        },
        error: function () {
            captchaError.text('Captcha verification failed. Try again.').show();
            submitButton.prop('disabled', false).text('Submit');
        }
    });
}
</script>
